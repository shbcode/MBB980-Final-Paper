def _require_csv(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(
            f"Required cached input is missing: {path}\n"
            f"Run the corresponding experiment first (see README.md)."
        )
    if path.stat().st_size == 0:
        raise ValueError(f"Required cached input is empty: {path}")
    return pd.read_csv(path)


def _round(value: float, ndigits: int = 3) -> float:
    if value is None or (isinstance(value, float) and (math.isnan(value) or math.isinf(value))):
        return value
    return round(float(value), ndigits)


def _fmt_p(p: float) -> str:
    """Format p-values: two-decimal for >=0.001, scientific for tiny."""
    if p is None or (isinstance(p, float) and math.isnan(p)):
        return "n/a"
    p = float(p)
    if p >= 0.001:
        return f"{p:.3f}"
    return f"{p:.1e}"


def _fmt_ci(lo: float, hi: float, ndigits: int = 2) -> str:
    return f"[{lo:.{ndigits}f}, {hi:.{ndigits}f}]"


def _escape_tex(s: object) -> str:
    """Minimal LaTeX escaping for string cells in tabular bodies."""
    if s is None:
        return ""
    if isinstance(s, float) and math.isnan(s):
        return ""
    text = str(s)
    # Order matters: backslash first.
    repls = [
        ("\\", r"\textbackslash{}"),
        ("&", r"\&"),
        ("%", r"\%"),
        ("$", r"\$"),
        ("#", r"\#"),
        ("_", r"\_"),
        ("{", r"\{"),
        ("}", r"\}"),
        ("~", r"\textasciitilde{}"),
        ("^", r"\textasciicircum{}"),
    ]
    for a, b in repls:
        text = text.replace(a, b)
    return text


def _df_to_tex(df: pd.DataFrame, *, caption: str | None = None,
               label: str | None = None) -> str:
    """Render a DataFrame as a simple booktabs-style LaTeX tabular.

    Strings are escaped; numeric cells are passed through as their `str(...)`.
    Designed to be pasted into a paper inside a `table` environment.
    """
    cols = list(df.columns)
    align = "l" + "r" * (len(cols) - 1) if len(cols) > 1 else "l"
    lines: list[str] = []
    lines.append("% Auto-generated by src.build_paper_assets — do not hand-edit.")
    if caption is not None or label is not None:
        lines.append(r"\begin{table}[t]")
        lines.append(r"  \centering")
    lines.append(rf"\begin{{tabular}}{{{align}}}")
    lines.append(r"\toprule")
    lines.append(" & ".join(_escape_tex(c) for c in cols) + r" \\")
    lines.append(r"\midrule")
    for _, row in df.iterrows():
        cells = [_escape_tex(row[c]) for c in cols]
        lines.append(" & ".join(cells) + r" \\")
    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    if caption is not None:
        # Caption is raw LaTeX prose — do not pass through _escape_tex.
        lines.append(rf"  \caption{{{caption}}}")
    if label is not None:
        lines.append(rf"  \label{{{label}}}")
    if caption is not None or label is not None:
        lines.append(r"\end{table}")
    return "\n".join(lines) + "\n"


def _write_triplet(df_csv: pd.DataFrame, df_pretty: pd.DataFrame,
                   base: Path, *, caption: str | None = None,
                   label: str | None = None) -> tuple[Path, Path, Path]:
    """Write a table as CSV (raw), Markdown (pretty), and LaTeX (pretty).

    The raw CSV preserves numeric precision and any 'extras' columns the
    pretty version dropped or merged. The Markdown and LaTeX versions are
    publication-ready: `df_pretty` is expected to contain pre-formatted
    string cells (e.g. "1.89", "[1.86, 1.91]", "7.3e-08"), so we disable
    tabulate's numeric re-parsing to keep them intact.
    """
    base.parent.mkdir(parents=True, exist_ok=True)
    csv_path = base.with_suffix(".csv")
    md_path = base.with_suffix(".md")
    tex_path = base.with_suffix(".tex")
    df_csv.to_csv(csv_path, index=False)
    md_path.write_text(
        df_pretty.to_markdown(index=False, disable_numparse=True) + "\n",
        encoding="utf-8",
    )
    tex_path.write_text(
        _df_to_tex(df_pretty, caption=caption, label=label),
        encoding="utf-8",
    )
    return csv_path, md_path, tex_path


def _order_tokenizers(df: pd.DataFrame, col: str = "tokenizer") -> pd.DataFrame:
    """Re-order rows by TOKENIZER_ORDER, dropping unknown ones to the end."""
    present = [t for t in TOKENIZER_ORDER if t in set(df[col])]
    extras = [t for t in df[col] if t not in TOKENIZER_ORDER]
    cat = pd.Categorical(df[col], categories=present + extras, ordered=True)
    return df.assign(**{col: cat}).sort_values(col).reset_index(drop=True)


