@click.command()
@click.option("--results-dir", default="results", type=click.Path(file_okay=False),
              help="Directory containing cached experiment outputs.")
@click.option("--out", "out_dir", default="reports/paper_assets",
              type=click.Path(file_okay=False),
              help="Where to write paper-ready assets.")
def main(results_dir: str, out_dir: str) -> None:
    apply_style()
    root = Path(results_dir)
    out = Path(out_dir)
    out.mkdir(parents=True, exist_ok=True)

    audit = _require_csv(root / "tokenization_audit" / "summary_by_tokenizer.csv")
    fc = _require_csv(root / "fixed_context" / "summary.csv")
    cont = _require_csv(root / "radical_sensitivity" / "contingency_same_radical.csv")
    coef = _require_csv(root / "radical_sensitivity" / "logit_coefficients.csv")
    adapt = _require_csv(root / "tokenizer_adaptation" / "audit_before_after.csv")
    pt_summary = _require_csv(root / "paraphrase_token_perturb" / "summary.csv")
    pt_rows_path = root / "paraphrase_token_perturb" / "perturbations.csv"
    pt_rows = pd.read_csv(pt_rows_path) if pt_rows_path.exists() else None

    written: list[Path] = []
    written.extend(build_fig1_and_table1(audit, out))
    written.extend(build_fig2_and_appendix_fc(fc, out))
    written.extend(build_table2_radical(cont, coef, out))
    written.extend(build_fig3_adaptation(adapt, out))
    written.extend(build_perturbation_assets(pt_summary, pt_rows, out))

    captions_path = out / "captions.md"
    captions_path.write_text(CAPTIONS_MD, encoding="utf-8")
    written.append(captions_path)

    readme_path = out / "README.md"
    readme_path.write_text(README_MD, encoding="utf-8")
    written.append(readme_path)

    click.echo(f"Wrote {len(written)} files to {out.resolve()}:")
    for p in sorted(written):
        click.echo(f"  {p.relative_to(out.parent.parent) if out.parent.parent in p.parents else p}")


if __name__ == "__main__":
    main()
