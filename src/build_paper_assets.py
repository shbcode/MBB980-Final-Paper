def build_fig1_and_table1(audit: pd.DataFrame, out: Path) -> list[Path]:
    """Figure 1 + Table 1 + Appendix full-audit table (shared input)."""
    written: list[Path] = []
    df = _order_tokenizers(audit.copy())

    # ------ Figure 1 ----------------------------------------------------
    fig, ax = plt.subplots(figsize=(6.4, 3.6))
    x = np.arange(len(df))
    yerr = None
    if {"TP_ci_low", "TP_ci_high"}.issubset(df.columns):
        lower = (df["mean_TP"] - df["TP_ci_low"]).clip(lower=0).values
        upper = (df["TP_ci_high"] - df["mean_TP"]).clip(lower=0).values
        yerr = np.vstack([lower, upper])
    bars = ax.bar(
        x, df["mean_TP"].values,
        yerr=yerr, capsize=3,
        color="#cccccc", edgecolor="black", linewidth=0.8,
    )
    # Highlight bars by parity status without abandoning grayscale-friendliness.
    for bar, tp in zip(bars, df["mean_TP"].values):
        if tp > 1.10:
            bar.set_hatch("///")
        elif tp < 0.90:
            bar.set_hatch("...")
    ax.axhline(1.0, linestyle="--", color="black", linewidth=0.9, zorder=0)
    ax.set_xticks(x)
    ax.set_xticklabels([TOKENIZER_LABELS.get(t, t) for t in df["tokenizer"]],
                       rotation=20, ha="right")
    ax.set_ylabel("Mean tokenization parity (ZH / EN)")
    ax.set_ylim(0, max(2.1, float(df["mean_TP"].max()) * 1.1))
    written.extend(save_figure(fig, out / "fig1_tokenization_parity_by_tokenizer"))

    # ------ Table 1 (main) ---------------------------------------------
    main = df[["tokenizer", "mean_TP", "TP_ci_low", "TP_ci_high",
               "mean_en_tokens", "mean_zh_tokens"]].copy()
    raw = main.copy()
    raw[["mean_TP", "TP_ci_low", "TP_ci_high"]] = raw[
        ["mean_TP", "TP_ci_low", "TP_ci_high"]
    ].round(3)
    raw[["mean_en_tokens", "mean_zh_tokens"]] = raw[
        ["mean_en_tokens", "mean_zh_tokens"]
    ].round(2)

    pretty = pd.DataFrame({
        "Tokenizer": [TOKENIZER_LABELS.get(t, t) for t in main["tokenizer"]],
        "Mean TP": [f"{v:.2f}" for v in main["mean_TP"]],
        "95% CI": [_fmt_ci(lo, hi) for lo, hi in
                   zip(main["TP_ci_low"], main["TP_ci_high"])],
        "Mean EN tokens": [f"{v:.1f}" for v in main["mean_en_tokens"]],
        "Mean ZH tokens": [f"{v:.1f}" for v in main["mean_zh_tokens"]],
    })
    written.extend(_write_triplet(
        raw, pretty, out / "table1_tokenization_audit_summary",
        caption="Tokenization audit summary on FLORES-200 EN-ZH devtest "
                "(N = 1012 paired sentences). TP = mean ZH tokens / EN tokens; "
                "95\\% CI from a paired bootstrap with 1000 resamples.",
        label="tab:audit-main",
    ))

    # ------ Appendix table J (full audit) ------------------------------
    full_cols = ["tokenizer", "mean_TP", "TP_ci_low", "TP_ci_high",
                 "mean_context_shrinkage", "mean_en_tokens", "mean_zh_tokens",
                 "wilcoxon_pvalue"]
    missing = [c for c in full_cols if c not in df.columns]
    if missing:
        raise KeyError(f"summary_by_tokenizer.csv missing expected columns: {missing}")
    full = df[full_cols].copy()
    full_raw = full.copy()
    full_raw[["mean_TP", "TP_ci_low", "TP_ci_high",
              "mean_context_shrinkage"]] = full_raw[
        ["mean_TP", "TP_ci_low", "TP_ci_high", "mean_context_shrinkage"]
    ].round(3)
    full_raw[["mean_en_tokens", "mean_zh_tokens"]] = full_raw[
        ["mean_en_tokens", "mean_zh_tokens"]
    ].round(2)
    full_pretty = pd.DataFrame({
        "Tokenizer": [TOKENIZER_LABELS.get(t, t) for t in full["tokenizer"]],
        "Mean TP": [f"{v:.2f}" for v in full["mean_TP"]],
        "95% CI": [_fmt_ci(lo, hi) for lo, hi in
                   zip(full["TP_ci_low"], full["TP_ci_high"])],
        "Context shrinkage": [f"{v:+.2f}" for v in full["mean_context_shrinkage"]],
        "Mean EN tokens": [f"{v:.1f}" for v in full["mean_en_tokens"]],
        "Mean ZH tokens": [f"{v:.1f}" for v in full["mean_zh_tokens"]],
        "Wilcoxon p": [_fmt_p(p) for p in full["wilcoxon_pvalue"]],
    })
    written.extend(_write_triplet(
        full_raw, full_pretty, out / "appendix_table_full_audit_with_controls",
        caption="Full per-tokenizer audit including context-shrinkage "
                "(1 - 1/TP) and Wilcoxon signed-rank p-values testing "
                "EN-vs-ZH token-count equality within each tokenizer.",
        label="tab:audit-full",
    ))
    return written


def build_fig2_and_appendix_fc(fc: pd.DataFrame, out: Path) -> list[Path]:
    """Figure 2 (examples-fit), appendix figure (accuracy), appendix table."""
    written: list[Path] = []
    needed = {"language", "budget", "examples_fit_mean", "accuracy",
              "cost_per_correct", "n_queries", "tokens_in", "tokens_out"}
    missing = needed.difference(fc.columns)
    if missing:
        raise KeyError(f"fixed_context summary.csv missing columns: {sorted(missing)}")

    pivot_fit = fc.pivot_table(index="budget", columns="language",
                               values="examples_fit_mean", aggfunc="mean").sort_index()
    pivot_acc = fc.pivot_table(index="budget", columns="language",
                               values="accuracy", aggfunc="mean").sort_index()

    # ------ Figure 2 ----------------------------------------------------
    fig, ax = plt.subplots(figsize=(5.6, 3.6))
    for lang in ("en", "zh"):
        if lang not in pivot_fit.columns:
            continue
        st = LANG_STYLE[lang]
        ax.plot(pivot_fit.index, pivot_fit[lang],
                marker=st["marker"], linestyle=st["linestyle"],
                color=st["color"], label=st["label"])
    ax.set_xlabel("Token budget")
    ax.set_ylabel("Mean few-shot demonstrations fit")
    ax.set_xticks(sorted(pivot_fit.index.unique()))
    ax.legend(loc="best")
    written.extend(save_figure(fig, out / "fig2_examples_fit_vs_budget"))

    # ------ Appendix figure G (accuracy) -------------------------------
    fig, ax = plt.subplots(figsize=(5.6, 3.6))
    for lang in ("en", "zh"):
        if lang not in pivot_acc.columns:
            continue
        st = LANG_STYLE[lang]
        ax.plot(pivot_acc.index, pivot_acc[lang],
                marker=st["marker"], linestyle=st["linestyle"],
                color=st["color"], label=st["label"])
    ax.set_xlabel("Token budget")
    ax.set_ylabel("Accuracy")
    ax.set_xticks(sorted(pivot_acc.index.unique()))
    ax.set_ylim(0.0, 1.0)
    ax.legend(loc="best")
    written.extend(save_figure(fig, out / "appendix_fig_accuracy_vs_budget"))

    # ------ Appendix table F -------------------------------------------
    cols = ["language", "budget", "examples_fit_mean", "accuracy",
            "cost_per_correct", "n_queries", "tokens_in", "tokens_out"]
    raw = fc[cols].copy().sort_values(["language", "budget"]).reset_index(drop=True)
    # Round only columns that look good at 3-decimals precision; leave
    # cost_per_correct alone because it is typically O(1e-5) and would round to 0.
    raw[["examples_fit_mean", "accuracy"]] = raw[
        ["examples_fit_mean", "accuracy"]
    ].round(3)
    pretty = pd.DataFrame({
        "Language": ["English" if l == "en" else "Chinese" for l in raw["language"]],
        "Budget": raw["budget"].astype(int),
        "Demos fit (mean)": [f"{v:.1f}" for v in raw["examples_fit_mean"]],
        "Accuracy": [f"{v:.3f}" for v in raw["accuracy"]],
        "Cost per correct (USD)": [
            "n/a" if pd.isna(v) else (f"{v:.6f}" if v >= 1e-4 else f"{v:.2e}")
            for v in raw["cost_per_correct"]
        ],
        "n queries": raw["n_queries"].astype(int),
        "Tokens in": raw["tokens_in"].astype(int),
        "Tokens out": raw["tokens_out"].astype(int),
    })
    written.extend(_write_triplet(
        raw, pretty, out / "appendix_table_fixed_context_full",
        caption="Full Experiment 2 (fixed-context) summary across token budgets "
                "and languages on XNLI few-shot prompts.",
        label="tab:fc-full",
    ))
    return written


def build_table2_radical(cont: pd.DataFrame, coef: pd.DataFrame, out: Path) -> list[Path]:
    """Table 2: compact radical/token sensitivity summary + appendix coef table."""
    written: list[Path] = []
    if cont.empty:
        raise ValueError("contingency_same_radical.csv is empty")
    if coef.empty:
        raise ValueError("logit_coefficients.csv is empty")

    c = cont.iloc[0]
    coef_idx = coef.set_index("term")
    same_rad = coef_idx.loc["same_radical"] if "same_radical" in coef_idx.index else None
    same_tok = coef_idx.loc["same_token"] if "same_token" in coef_idx.index else None
    regularized = bool(coef.get("regularized", pd.Series([False])).any())

    rows = [
        {"Quantity": "P(predicted similar | same radical)",
         "Estimate": f"{c['p_pred_yes_given_same_radical']:.3f}",
         "Detail": f"n={int(c['n_same_radical'])}"},
        {"Quantity": "P(predicted similar | different radical)",
         "Estimate": f"{c['p_pred_yes_given_diff_radical']:.3f}",
         "Detail": f"n={int(c['n_diff_radical'])}"},
        {"Quantity": "Odds ratio (same vs different radical)",
         "Estimate": f"{c['odds_ratio_haldane']:.2f}",
         "Detail": "Haldane-Anscombe corrected"},
        {"Quantity": "Fisher exact p",
         "Estimate": _fmt_p(c["fisher_exact_p"]),
         "Detail": "two-sided"},
        {"Quantity": "Logit β(same_radical)",
         "Estimate": "n/a" if same_rad is None else f"{same_rad['coef']:+.2f}",
         "Detail": "n/a" if same_rad is None else
                   ("regularized (no p)" if bool(same_rad.get("regularized", False))
                    else f"p = {_fmt_p(same_rad['p_value'])}")},
        {"Quantity": "Logit β(same_token)",
         "Estimate": "n/a" if same_tok is None else f"{same_tok['coef']:+.2f}",
         "Detail": "n/a" if same_tok is None else
                   ("regularized (no p)" if bool(same_tok.get("regularized", False))
                    else f"p = {_fmt_p(same_tok['p_value'])}")},
    ]
    pretty = pd.DataFrame(rows)
    raw = pretty.copy()  # already a tidy summary; same content for CSV
    caption = (
        "Behavioral evidence that token identity modulates the model's "
        "character-similarity judgments after controlling for radical "
        "(script) identity. Logit coefficients use cl100k\\_base as the "
        "reference tokenizer for the same\\_token contrast."
    )
    if regularized:
        caption += " Some coefficients used L2-regularised logistic regression; p-values are unavailable for those rows."
    written.extend(_write_triplet(
        raw, pretty, out / "table2_radical_token_sensitivity_summary",
        caption=caption, label="tab:radical-summary",
    ))

    # Appendix coefficient table — keep full precision for the raw CSV;
    # format strings for the pretty rendering. Rounding p-values before
    # formatting would round tiny ones (e.g. 4.8e-06) down to 0.
    coef_raw = coef.copy()
    coef_pretty = pd.DataFrame({
        "Term": coef_raw["term"],
        "β (coef)": [f"{v:+.3f}" for v in coef_raw["coef"]],
        "p value": [_fmt_p(p) for p in coef_raw["p_value"]],
        "Regularized": ["yes" if bool(r) else "no" for r in coef_raw["regularized"]],
    })
    written.extend(_write_triplet(
        coef_raw, coef_pretty, out / "appendix_table_radical_logit_coefficients",
        caption="Full logistic-regression coefficients for Experiment 3.",
        label="tab:radical-coefs",
    ))
    return written


def build_fig3_adaptation(adapt: pd.DataFrame, out: Path) -> list[Path]:
    """Figure 3: tokenizer adaptation counterfactual."""
    needed = {"added_tokens", "mean_TP"}
    missing = needed.difference(adapt.columns)
    if missing:
        raise KeyError(f"audit_before_after.csv missing columns: {sorted(missing)}")

    df = (adapt.drop_duplicates(subset=["added_tokens"])
                .sort_values("added_tokens").reset_index(drop=True))

    fig, ax = plt.subplots(figsize=(5.6, 3.6))
    ax.plot(df["added_tokens"], df["mean_TP"],
            marker="o", linestyle="-", color="#1f77b4", linewidth=1.8)
    ax.axhline(1.0, linestyle="--", color="black", linewidth=0.9, zorder=0)
    ax.set_xlabel("Added Chinese vocabulary pieces")
    ax.set_ylabel("Mean tokenization parity (ZH / EN)")
    # Sensible y-range that always shows both the line and the parity reference.
    y_lo = min(0.7, float(df["mean_TP"].min()) * 0.95)
    y_hi = max(1.15, float(df["mean_TP"].max()) * 1.05)
    ax.set_ylim(y_lo, y_hi)
    return list(save_figure(fig, out / "fig3_tokenizer_adaptation_counterfactual"))


def build_perturbation_assets(pt_summary: pd.DataFrame,
                              pt_rows: pd.DataFrame | None,
                              out: Path) -> list[Path]:
    """Appendix table H + appendix figure I."""
    written: list[Path] = []
    needed = {"transform", "mean_stability_exact", "mean_stability_jaccard",
              "mean_delta_tokens", "delta_accuracy"}
    missing = needed.difference(pt_summary.columns)
    if missing:
        raise KeyError(f"paraphrase summary.csv missing columns: {sorted(missing)}")

    raw = pt_summary[list(needed) + (["n"] if "n" in pt_summary.columns else [])].copy()
    raw = raw.sort_values("transform").reset_index(drop=True)
    raw[["mean_stability_exact", "mean_stability_jaccard",
         "mean_delta_tokens", "delta_accuracy"]] = raw[
        ["mean_stability_exact", "mean_stability_jaccard",
         "mean_delta_tokens", "delta_accuracy"]
    ].round(3)
    pretty = pd.DataFrame({
        "Transform": [TRANSFORM_LABELS.get(t, t) for t in raw["transform"]],
        "Stability (exact)": [f"{v:.3f}" for v in raw["mean_stability_exact"]],
        "Stability (Jaccard)": [f"{v:.3f}" for v in raw["mean_stability_jaccard"]],
        "Δ tokens": [f"{v:+.2f}" for v in raw["mean_delta_tokens"]],
        "Δ accuracy": [f"{v:+.3f}" for v in raw["delta_accuracy"]],
    })
    written.extend(_write_triplet(
        raw, pretty, out / "appendix_table_perturbation_summary",
        caption="Experiment 4: response stability and accuracy delta under "
                "meaning-preserving paraphrases and surface-level token "
                "perturbations. Identity-row stability is the temperature=0 "
                "non-determinism floor.",
        label="tab:perturb-summary",
    ))

    fig, ax = plt.subplots(figsize=(6.0, 3.6))
    if pt_rows is not None and "stability_jaccard" in pt_rows.columns:
        # Box plot keeps the per-prompt distribution visible.
        order = sorted(pt_rows["transform"].unique(),
                       key=lambda t: list(TRANSFORM_LABELS.keys()).index(t)
                       if t in TRANSFORM_LABELS else 999)
        data = [pt_rows.loc[pt_rows["transform"] == t, "stability_jaccard"].values
                for t in order]
        bp = ax.boxplot(data, tick_labels=[TRANSFORM_LABELS.get(t, t) for t in order],
                        patch_artist=True, widths=0.55)
        for patch in bp["boxes"]:
            patch.set_facecolor("#dddddd")
            patch.set_edgecolor("black")
        for med in bp["medians"]:
            med.set_color("black")
        ax.set_ylabel("Stability (Jaccard)")
    else:
        x = np.arange(len(raw))
        ax.bar(x, raw["mean_stability_jaccard"].values,
               color="#cccccc", edgecolor="black", linewidth=0.8)
        ax.set_xticks(x)
        ax.set_xticklabels([TRANSFORM_LABELS.get(t, t) for t in raw["transform"]],
                           rotation=20, ha="right")
        ax.set_ylabel("Mean stability (Jaccard)")
    ax.set_ylim(0.0, 1.05)
    plt.setp(ax.get_xticklabels(), rotation=20, ha="right")
    written.extend(save_figure(fig, out / "appendix_fig_perturbation_stability"))
    return written


